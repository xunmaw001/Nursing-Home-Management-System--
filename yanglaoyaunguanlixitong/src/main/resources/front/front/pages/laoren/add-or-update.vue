            jiashuIndex : 0,//家属下标

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
            let yonghuParams = {
                page: 1,
                limit: 100,
            }
            let yonghuData = await this.$api.page(`yonghu`, yonghuParams);
            this.yonghuOptions = yonghuData.data.list;
            let chuangweiParams = {
                page: 1,
                limit: 100,
            }
            let chuangweiData = await this.$api.page(`chuangwei`, chuangweiParams);
            this.chuangweiOptions = chuangweiData.data.list;
            let jiashuParams = {
                page: 1,
                limit: 100,
            }
            let jiashuData = await this.$api.page(`jiashu`, jiashuParams);
            this.jiashuOptions = jiashuData.data.list;
		/*下拉框*/
			let sexTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'sex_types',
            }
			let sexTypes = await this.$api.page(`dictionary`, sexTypesParams);
			this.sexTypesOptions = sexTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`laoren`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.laorenId){
                this.ruleForm.laorenId = options.laorenId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            yonghuChange(e) {
                this.yonghuIndex = e.target.value
                this.ruleForm.yonghuName = this.yonghuOptions[this.yonghuIndex].yonghuName
                this.ruleForm.yonghuId = this.yonghuOptions[this.yonghuIndex].id
            },
            chuangweiChange(e) {
                this.chuangweiIndex = e.target.value
                this.ruleForm.chuangweiName = this.chuangweiOptions[this.chuangweiIndex].chuangweiName
                this.ruleForm.chuangweiId = this.chuangweiOptions[this.chuangweiIndex].id
            },
            jiashuChange(e) {
                this.jiashuIndex = e.target.value
                this.ruleForm.jiashuName = this.jiashuOptions[this.jiashuIndex].jiashuName
                this.ruleForm.jiashuId = this.jiashuOptions[this.jiashuIndex].id
            },
            // 下拉变化
            sexTypesChange(e) {
                this.sexTypesIndex = e.target.value
                this.ruleForm.sexValue = this.sexTypesOptions[this.sexTypesIndex].indexName
                this.ruleForm.sexTypes = this.sexTypesOptions[this.sexTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
