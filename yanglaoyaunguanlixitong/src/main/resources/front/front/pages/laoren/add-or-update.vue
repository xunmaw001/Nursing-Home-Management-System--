            // 	})
            // },
			laorenPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.laorenPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.kanhurenName)) {
                    this.$utils.msg(`看护人姓名不能为空`);
                    return
                }
				if ((!this.ruleForm.kanhurenPhone) && (!this.$validate.isMobile(this.ruleForm.kanhurenPhone))) {
                    this.$utils.msg(`看护人联系方式不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.laorenName)) {
                    this.$utils.msg(`老人姓名不能为空`);
                    return
                }
				if ((!this.ruleForm.laorenPhone) && (!this.$validate.isMobile(this.ruleForm.laorenPhone))) {
                    this.$utils.msg(`老人手机号不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.laorenIdNumber) && (!this.$validate.checkIdCard(this.ruleForm.laorenIdNumber))) {
                    this.$utils.msg(`老人身份证号不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.sexTypes)) {
                    this.$utils.msg(`性别不能为空`);
                    return
                }
				if ((!this.ruleForm.age) && (!this.$validate.isIntNumer(this.ruleForm.age)) && this.ruleForm.age >0) {
                    this.$utils.msg(`年龄不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.laorenPhoto)) {
                    this.$utils.msg(`老人照片不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`laoren`, this.ruleForm);
                } else {
                    await this.$api.save(`laoren`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
