            },
            // 登陆用户信息
            user: {},
            fangjianTypesOptions: [],
            fangjianTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let fangjianTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'fangjian_types',
            }
			let fangjianTypes = await this.$api.page(`dictionary`, fangjianTypesParams);
			this.fangjianTypesOptions = fangjianTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`fangjian`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.fangjianId){
                this.ruleForm.fangjianId = options.fangjianId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            fangjianTypesChange(e) {
                this.fangjianTypesIndex = e.target.value
                this.ruleForm.fangjianValue = this.fangjianTypesOptions[this.fangjianTypesIndex].indexName
                this.ruleForm.fangjianTypes = this.fangjianTypesOptions[this.fangjianTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			fangjianPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.fangjianPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.fangjianName)) {
                    this.$utils.msg(`房间名称不能为空`);
                    return
                }
				if ((!this.ruleForm.fangjianUuidNumber)) {
                    this.$utils.msg(`房间编号不能为空`);
                    return
                }
				if ((!this.ruleForm.fangjianPhoto)) {
                    this.$utils.msg(`房间照片不能为空`);
                    return
                }
				if ((!this.ruleForm.fangjianAddress)) {
                    this.$utils.msg(`房间地点不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`fangjian`, this.ruleForm);
                } else {
                    await this.$api.save(`fangjian`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
