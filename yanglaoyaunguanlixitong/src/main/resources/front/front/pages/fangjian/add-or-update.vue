    export default {
        data() {
            return {
                cross: '',
                ro:{
					fangjianName: false,
					fangjianUuidNumber: true,
					fangjianPhoto: false,
					fangjianAddress: false,
					fangjianTypes: false,
					fangjianContent: false,
					fangjianDelete: false,
					insertTime: false,
					createTime: false,
        },
            ruleForm: {
					fangjianName: '',
					fangjianUuidNumber: this.getUUID(),//数字
					fangjianPhoto: '',
					fangjianAddress: '',
					fangjianTypes: '',//数字
					fangjianValue: '',//数字对应的值
					fangjianContent: '',
					fangjianDelete: '',
					insertTime: '',
